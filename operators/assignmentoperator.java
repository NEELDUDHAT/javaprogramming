public class assignmentoperator {
    public static void main(String[] args) {
        int p = 10, q;

        // use of = operator
        q = p;
        System.out.println(q);

        // // use of += operator
        p += q; // p=p+q
        System.out.println(p);

        // use of -= operator
        p -= q; // p=p-q
        System.out.println(p);

        // use of *= operator
        p *= q; // p=p*q
        System.out.println(p);

        // use of /= operator
        p /= q; // p=p/q
        System.out.println(p);

    }
}
